/*
 * Unary Operator
 * Unary operators operate on a single operand.
 * Common unary operators include the increment (++), decrement (--), and negation (-) operators.
 */

/*
 * Increment Operator (++)
 */
let x = 5;
console.log(++x); // Output: 6 (pre-increment)
console.log(x++); // Output: 6 (post-increment, x becomes 7 after this line)
console.log(x);   // Output: 7

/*
 * Decrement Operator (--)
 */
let y = 5;
console.log(--y); // Output: 4 (pre-decrement)
console.log(y--); // Output: 4 (post-decrement, y becomes 3 after this line)
console.log(y);   // Output: 3

/*
 * Negation Operator (-)
 */
let z = 5;
console.log(-z); // Output: -5