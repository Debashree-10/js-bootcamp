/*
 * Arithmetic Operators
 * These operators perform mathematical calculations.
 */

let a = 10;
let b = 5;

/*
 * Addition (+): Adds two numbers or concatenates strings.
 */
let sum = a + b; // 15
console.log("Addition:", sum); // Output: Addition: 15

let str1 = "Hello, ";
let str2 = "World!";
let greeting = str1 + str2; // "Hello, World!"
console.log("String Concatenation:", greeting); // Output: String Concatenation: Hello, World!

/*
 * Subtraction (-): Subtracts the second number from the first.
 */
let difference = a - b; // 5
console.log("Subtraction:", difference); // Output: Subtraction: 5

/*
 * Multiplication (*): Multiplies two numbers.
 */
let product = a * b; // 50
console.log("Multiplication:", product); // Output: Multiplication: 50

/*
 * Division (/): Divides the first number by the second. 
 */
let quotient = a / b; // 2
console.log("Division:", quotient); // Output: Division: 2

/*
 * Modulus (%): Returns the remainder of the division of the first number by the second.
 */
let remainder = a % b; // 0
console.log("Modulus (10 % 5):", remainder); // Output: Modulus (10 % 5): 0

let c = 7;
let remainder2 = c % b; // 2
console.log("Modulus (7 % 5):", remainder2); // Output: Modulus (7 % 5): 2

/*
 * Exponentiation (**): Raises the first number to the power of the second number.
 */
let power = a ** 2; // 100
console.log("Exponentiation (10^2):", power); // Output: Exponentiation (10^2): 100

let power2 = 2 ** 3; // 8
console.log("Exponentiation (2^3):", power2); // Output: Exponentiation (2^3): 8