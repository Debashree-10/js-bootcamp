/*
 * Assignment Operators in JavaScript
 * Assignment operators are used to assign values to variables.
 */

/*
 * Simple Assignment Operator (=)
 * The simple assignment operator assigns the value on the right to the variable on the left.
 */
let x = 10; // Assigns 10 to x
console.log("Simple Assignment:", x); // Output: Simple Assignment: 10

/*
 * Addition Assignment Operator (+=)
 * The addition assignment operator adds the right operand to the left operand and assigns the result to the left operand. 
 */
let y = 5;
y += 3; // Equivalent to y = y + 3
console.log("Addition Assignment:", y); // Output: Addition Assignment: 8

/*
 * Subtraction Assignment Operator (-=)
 * The subtraction assignment operator subtracts the right operand from the left operand and assigns the result to the left operand.
 */
let z = 10;
z -= 4; // Equivalent to z = z - 4
console.log("Subtraction Assignment:", z); // Output: Subtraction Assignment: 6

/*
 * Multiplication Assignment Operator (*=)
 * The multiplication assignment operator multiplies the left operand by the right operand and assigns the result to the left operand.
 */
let a = 5;
a *= 2; // Equivalent to a = a * 2
console.log("Multiplication Assignment:", a); // Output: Multiplication Assignment: 10

/*
 * Division Assignment Operator (/=)
 * The division assignment operator divides the left operand by the right operand and assigns the result to the left operand.
 */
let b = 20;
b /= 4; // Equivalent to b = b / 4
console.log("Division Assignment:", b); // Output: Division Assignment: 5

/*
 * Modulus Assignment Operator (%=)
 * The modulus assignment operator takes the modulus using the left and right operands and assigns the result to the left operand.
 */
let c = 10;
c %= 3; // Equivalent to c = c % 3
console.log("Modulus Assignment:", c); // Output: Modulus Assignment: 1

/*
 * Exponentiation Assignment Operator (**=)
 * The exponentiation assignment operator raises the left operand to the power of the right operand and assigns the result to the left operand.
 */
let d = 2;
d **= 3; // Equivalent to d = d ** 3
console.log("Exponentiation Assignment:", d); // Output: Exponentiation Assignment: 8