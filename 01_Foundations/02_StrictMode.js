/*
 * How to Use "use strict"; 
 */

// Globally: By placing "use strict"; at the top of a JavaScript file, it applies to the entire file.
"use strict";
// All code in this file is in strict mode

// Locally: By placing "use strict"; inside a function, it applies only to that function.
function myFunction() {
    "use strict";
    // Code here is in strict mode
}